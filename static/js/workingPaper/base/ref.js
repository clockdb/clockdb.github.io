function ReferenceVisibility() {
    a = document.getElementsByClassName('Reference')
    if (a.length > 0) {
        document.getElementById('ReferenceHeaderRow').style.display = '';
        for(let i = 0; i < a.length; i++) {
            a[i].style.display = '';
        }
    }
}


